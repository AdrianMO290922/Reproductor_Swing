    public void controlAudio(File ruta) throws UnsupportedAudioFileException, IOException, LineUnavailableException    {
            AudioInputStream audio = AudioSystem.getAudioInputStream(ruta);
            clip = AudioSystem.getClip();
            clip.open(audio);
    }
